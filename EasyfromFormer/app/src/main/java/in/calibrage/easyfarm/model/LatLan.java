package in.calibrage.easyfarm.model;

public class LatLan{
    private String lat,lan;

    public LatLan(String lat, String lan) {
        this.lat = lat;
        this.lan = lan;
    }

    public String getLat() {
        return lat;
    }

    public void setLat(String lat) {
        this.lat = lat;
    }

    public String getLan() {
        return lan;
    }

    public void setLan(String lan) {
        this.lan = lan;
    }
};